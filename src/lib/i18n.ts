export type TranslationKey = keyof typeof translations.nb;

export function t(lang: Lang, key: TranslationKey, params?: Record<string, string>): string {
  let text: string = translations[lang][key] || translations.nb[key] || key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, v);
    });
  }
  return text;
}
